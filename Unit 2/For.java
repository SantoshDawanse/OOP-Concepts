public class For {
  public static void main(String[] args) {

    // for loop
    for (int x = 1; x <= 10; x++) {
      if (x == 5) {
        continue;
      }
      System.out.println(x);
    }

    // selection statements, iteration statements and jump statements => control statements
    // selection statements => if, else-if, nested-if, else-if ladder, switch
    // iteration statements => for, while, do-while
    // jump statements => break, continue, return
  }
}
